import type { ChatMessage, ChatStatus } from "../types";

export const initialMessages: ChatMessage[] = [];

export function statusLabel(status: ChatStatus) {
  switch (status) {
    case "idle":
      return "空闲";
    case "submitting":
      return "连接中";
    case "streaming":
      return "接收中";
    case "error":
      return "出错";
  }
}
